
package netease.bigdata.hivetools.meta;


public class Types {

    private Long types_id;
    private String type_name;
    private String type1;
    private String type2;

    public void Types() {
    }

    public void setTypesId(Long typesId_) {
        types_id = typesId_;
    }

    public Long getTypesId() {
        return types_id;
    }

    public void setTypeName(String typeName_) {
        type_name = typeName_;
    }

    public String getTypeName() {
        return type_name;
    }

    public void setType1(String type1_) {
        type1 = type1_;
    }

    public String getType1() {
        return type1;
    }

    public void setType2(String type2_) {
        type2 = type2_;
    }

    public String getType2() {
        return type2;
    }

}
